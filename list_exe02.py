# Desenvolva um programa que localize e apresente o maior e o menor número em uma lista de valores numéricos.

# Exemplo de Lista: [12, 5, 89, 3, 27, 44]

# Saída Esperada: Maior: 89, Menor: 3

n_list: list = [12, 5, 89, 3, 27, 44]

highest_n = max(n_list)
lowest_n = min(n_list)
print(f"Highest number: {highest_n} and lowest {lowest_n}")
